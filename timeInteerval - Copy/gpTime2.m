function [TM]=gpTime2(tm)
n=size(tm);
n1=n(1,1);
X=[];
K=1;
while K<=n1
oo1=tm(K,1);
oo2=table2cell(oo1); %oo2 is a cell array
oo3=string(oo2);% oo3 is a string
oo3=char(oo3);
u1=str2num(oo3(1,1:2));%hh
u2=str2num(oo3(4:5));%mm
u3=str2num(oo3(7:end));%ss
x=[u1,u2,u3];
X=[X;x];
    K=K+1;
end%while K<=n1
TM=duration(X,'Format','hh:mm:ss.SSS')